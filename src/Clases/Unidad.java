package Clases;

/**
 *
 * @author Gaston
 */
public class Unidad {
    
    int idUnidad;
    int planchaEntera;
    int planchaTresCuarto;
    int plachaUnCuarto;
    int planchaMedia;

    public Unidad() {
    }

    public int getIdUnidad() {
        return idUnidad;
    }

    public void setIdUnidad(int idUnidad) {
        this.idUnidad = idUnidad;
    }

    public int getPlanchaEntera() {
        return planchaEntera;
    }

    public void setPlanchaEntera(int planchaEntera) {
        this.planchaEntera = planchaEntera;
    }

    public int getPlanchaTresCuarto() {
        return planchaTresCuarto;
    }

    public void setPlanchaTresCuarto(int planchaTresCuarto) {
        this.planchaTresCuarto = planchaTresCuarto;
    }

    public int getPlachaUnCuarto() {
        return plachaUnCuarto;
    }

    public void setPlachaUnCuarto(int plachaUnCuarto) {
        this.plachaUnCuarto = plachaUnCuarto;
    }

    public int getPlanchaMedia() {
        return planchaMedia;
    }

    public void setPlanchaMedia(int planchaMedia) {
        this.planchaMedia = planchaMedia;
    }
    
    
    
    
}
