     void total(){
                   try{
            String dato [] = new String [1];
            Conectar miconexion = new Conectar();
            String consultasql = "SELECT SUM(monto) FROM movimiento_cajaing WHERE fecha = CURDATE()";
            ResultSet rs = miconexion.consulta(consultasql);  
            
            while(rs.next()){       //recorre el resultset mientras tenga algun dato
                for (int j=0; j<dato.length; j++){  //RECORRE HORINZONTALMENTE
                    dato[j]= rs.getString(j+1);
                }
                txttotal.setText(rs.getString(1));
                Gestionar_Caja.iFCaja.txtingreso.setText(txttotal.getText());
                
                           }   
        }
         catch(Exception e){
           //e.toString() es para que te mustre el error si lo tenes 
           JOptionPane.showMessageDialog(null, e.toString()); 
        }
       }
      
    
    void cargar(String valor){
    String mostrar="Select * FROM movimiento_cajaing WHERE fecha = CURDATE()";
    String titulos[] = {"Nro","detalle","monto","fecha"};
    String []Registros=new String[4];
    model = new DefaultTableModel(null,titulos);
  
        try {
              Statement st = cn.createStatement();
              ResultSet rs = st.executeQuery(mostrar);
              while(rs.next())
              {
                  Registros[0]= rs.getString("idingreso");
                  Registros[1]= rs.getString("detalle");
                  Registros[2]= rs.getString("monto");
                  Registros[3]= rs.getString("fecha");
                       model.addRow(Registros);
              }
              grillaVentas.setModel(model);
              
        } catch (SQLException ex) {
            Logger.getLogger(iFDescripcionArticulos.class.getName()).log(Level.SEVERE, null, ex);
        }
    
  }  
     
     

